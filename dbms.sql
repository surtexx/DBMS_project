create or replace type tablou IS TABLE OF NUMBER;

CREATE OR REPLACE PROCEDURE number_of_goals (players IN tablou) IS
    gol NUMBER;
    type vector is TABLE OF NUMBER INDEX BY PLS_INTEGER;
    goals vector;
    BEGIN
        FOR i in players.FIRST..players.LAST LOOP
            SELECT STATISTICI_INDIVIDUALE.numar_goluri
            INTO gol
            FROM STATISTICI_INDIVIDUALE, JUCATOR
            WHERE JUCATOR.id_jucator = players(i) AND 
            STATISTICI_INDIVIDUALE.id_statistici = JUCATOR.id_statistici;
            goals(i) := gol;
        END LOOP;
        FOR i in goals.FIRST..goals.LAST LOOP
            DBMS_OUTPUT.PUT_LINE('Jucatorul cu ID ' || players(i) || ' a inscris ' 
            || goals(i) || ' goluri.');
        END LOOP;
END;

