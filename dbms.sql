CREATE TABLE SPONSOR(
    id_sponsor NUMBER NOT NULL,
    nume_sponsor VARCHAR2(30) NOT NULL,
    data_expirare_contract DATE NOT NULL,
    venit_anual NUMBER NOT NULL,
    PRIMARY KEY (id_sponsor)
);

CREATE TABLE CAMPIONAT(
id_campionat NUMBER NOT NULL,
id_sponsor NUMBER,
tara VARCHAR2(30) NOT NULL,
an_infiintare_campionat NUMBER NOT NULL,
rang NUMBER NOT NULL,
PRIMARY KEY (id_campionat),
CONSTRAINT fk_sponsor
FOREIGN KEY (id_sponsor)
REFERENCES sponsor(id_sponsor)
);

CREATE TABLE DIVIZIE(
id_divizie NUMBER NOT NULL,
id_campionat NUMBER NOT NULL,
tier NUMBER NOT NULL,
nume VARCHAR2(50),
numar_echipe NUMBER NOT NULL,
numar_locuri_promovare NUMBER NOT NULL,
numar_locuri_retrogradare NUMBER NOT NULL,
PRIMARY KEY(id_divizie),
CONSTRAINT fk_campionat
FOREIGN KEY (id_campionat)
REFERENCES CAMPIONAT(id_campionat)
);

CREATE TABLE ARBITRU(
id_arbitru NUMBER NOT NULL,
id_divizie NUMBER NOT NULL,
nume_arbitru VARCHAR2(30) NOT NULL,
central NUMBER(1) NOT NULL,
asistent NUMBER(1) NOT NULL,
tusier NUMBER(1) NOT NULL,
licenta_continentala NUMBER(1) NOT NULL,
PRIMARY KEY (id_arbitru),
CONSTRAINT fk_divizie
FOREIGN KEY (id_divizie)
REFERENCES DIVIZIE(id_divizie)
);

CREATE TABLE STADION(
id_stadion NUMBER NOT NULL,
id_campionat NUMBER NOT NULL,
nume_stadion VARCHAR2(40) NOT NULL,
numar_locuri NUMBER NOT NULL,
tip_gazon VARCHAR2(9) NOT NULL,
PRIMARY KEY (id_stadion),
CONSTRAINT fk_campionat2
FOREIGN KEY (id_campionat)
REFERENCES CAMPIONAT(id_campionat),
CHECK (tip_gazon IN('sintetic', 'natural', 'mix'))
);

CREATE TABLE STATISTICI_INDIVIDUALE(
id_statistici NUMBER NOT NULL,
numar_goluri NUMBER NOT NULL,
numar_assisturi NUMBER NOT NULL,
numar_galbene NUMBER NOT NULL,
numar_rosii NUMBER NOT NULL,
PRIMARY KEY(id_statistici)
);

CREATE TABLE LICENTA(
id_licenta NUMBER NOT NULL,
denumire_licenta VARCHAR2(30) NOT NULL,
dificultate NUMBER NOT NULL,
PRIMARY KEY (id_licenta),
CHECK (dificultate > 0 AND dificultate < 101)
);

CREATE TABLE ANTRENOR(
id_antrenor NUMBER NOT NULL,
nume_antrenor VARCHAR2(30) NOT NULL,
id_licenta NUMBER NOT NULL,
data_nastere_antrenor DATE NOT NULL,
PRIMARY KEY (id_antrenor),
CONSTRAINT fk_licenta
FOREIGN KEY(id_licenta)
REFERENCES LICENTA(id_licenta)
);

CREATE TABLE ECHIPA(
id_echipa NUMBER NOT NULL,
id_divizie NUMBER NOT NULL,
nume_echipa VARCHAR2(30) NOT NULL,
an_infiintare_echipa NUMBER NOT NULL,
numar_trofee NUMBER NOT NULL,
id_antrenor NUMBER NOT NULL,
PRIMARY KEY (id_echipa),
CONSTRAINT fk_divizie2
FOREIGN KEY(id_divizie)
REFERENCES DIVIZIE(id_divizie),
CONSTRAINT fk_antrenor
FOREIGN KEY(id_antrenor)
REFERENCES ANTRENOR(id_antrenor) 
);

CREATE TABLE PARTIDA(
id_partida NUMBER NOT NULL,
id_echipa_gazda NUMBER NOT NULL,
id_echipa_oaspete NUMBER NOT NULL,
id_stadion NUMBER NOT NULL,
nr_goluri_gazda NUMBER NOT NULL,
nr_goluri_oaspeti NUMBER NOT NULL,
numar_spectatori NUMBER NOT NULL,
PRIMARY KEY(id_partida),
CONSTRAINT fk_echipa_gazda
FOREIGN KEY(id_echipa_gazda)
REFERENCES ECHIPA(id_echipa),
CONSTRAINT fk_echipa_oaspete
FOREIGN KEY(id_echipa_oaspete)
REFERENCES ECHIPA(id_echipa),
CONSTRAINT fk_stadion
FOREIGN KEY(id_stadion)
REFERENCES STADION(id_stadion) 
);

CREATE TABLE ARBITRU_PARTIDA(
id_partida NUMBER NOT NULL,
id_arbitru NUMBER NOT NULL,
PRIMARY KEY (id_partida, id_arbitru),
CONSTRAINT fk_arbitru
FOREIGN KEY(id_arbitru)
REFERENCES ARBITRU(id_arbitru),
CONSTRAINT fk_partida
FOREIGN KEY(id_partida)
REFERENCES PARTIDA(id_partida) 
);

CREATE TABLE JUCATOR(
id_jucator NUMBER NOT NULL,
nume_jucator VARCHAR2(30) NOT NULL,
id_echipa NUMBER NOT NULL,
pozitie VARCHAR2(8) NOT NULL,
id_statistici NUMBER NOT NULL,
data_nastere_jucator DATE NOT NULL,
picior VARCHAR2(5) NOT NULL,
PRIMARY KEY (id_jucator),
CONSTRAINT fk_echipa
FOREIGN KEY(id_echipa)
REFERENCES ECHIPA(id_echipa),
CONSTRAINT fk_statistici
FOREIGN KEY(id_statistici)
REFERENCES STATISTICI_INDIVIDUALE(id_statistici),
CHECK (picior IN('stang','drept')),
CHECK (pozitie IN ('portar','fundas','mijlocas','atacant'))
);

