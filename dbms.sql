-- 7. Pentru fiecare jucator din divizia Premier League (id_divizie=1) sa se
-- calculeze contributia sa la goluri (goluri + pase de gol) si sa se afiseze

CREATE OR REPLACE PROCEDURE contributii IS
    CURSOR prem_players IS
        SELECT JUCATOR.id_jucator id
        FROM JUCATOR, ECHIPA
        WHERE JUCATOR.id_echipa = ECHIPA.id_echipa AND ECHIPA.id_divizie = 1;
    CURSOR contributie(j_id NUMBER) IS
        SELECT STATISTICI_INDIVIDUALE.numar_goluri + STATISTICI_INDIVIDUALE.numar_assisturi
        FROM STATISTICI_INDIVIDUALE, JUCATOR
        WHERE STATISTICI_INDIVIDUALE.id_statistici = JUCATOR.id_statistici AND
            JUCATOR.id_jucator = j_id;
    g_a NUMBER;
    BEGIN
        FOR p_id IN prem_players LOOP
            OPEN contributie(p_id.id);
            LOOP
                FETCH contributie INTO g_a;
                EXIT WHEN contributie%NOTFOUND;
                DBMS_OUTPUT.PUT_LINE(p_id.id || ' cu contributia ' || g_a);
            END LOOP;
            CLOSE contributie;
        END LOOP;
    END;

BEGIN
    contributii();
END;

